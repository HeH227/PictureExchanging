@implementation SidebarTableViewController

- (instancetype)initWithStyle:(UITableViewStyle)style
{
    if(self == [super initWithStyle:style]){
        self.view.frame = CGRectMake(0, 64, 0, [UIScreen mainScreen].bounds.size.height-108);
        self.view.backgroundColor = [UIColor whiteColor];
    }
    return self;
}

- (void)viewDidLoad {
    [super viewDidLoad];
    
    [self.tableView registerClass:[SidebarTableViewCell class] forCellReuseIdentifier:@"sidebarCell"];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return 3;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"sidebarCell" forIndexPath:indexPath];
    
    cell.textLabel.text = @"1";
    
    return cell;
}

